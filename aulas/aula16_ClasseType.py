# CLASSE TYPE

# A classe type é uma classe embutida que é usada para representar os tipos de dados dos objetos. 
# Ela pode ser usada para determinar o tipo de um objeto em tempo de execução e também para criar novos tipos de dados dinamicamente.

# Por enquanto, não se preocupe muito com a classe type. 
# Apesar de ser uma parte fundamental da linguagem Python, sendo utilizada implicitamente em muitas situações, como na determinação de tipos de variáveis e na criação de classes e objetos, nós a usaremos, por hora, apenas para exemplificar os tipos de dados. 
# Voltaremos a falar sobre classes e a classe type mais a frente.

# Sintaxe: type(object)

print(type('Python'))
print(type(0))
print(type(1.9))
print(type(-1.0), type(0.0))
print(type(bool(0)))