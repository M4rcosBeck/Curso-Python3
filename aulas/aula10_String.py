# STRING

# Em Python, uma string (str) é uma sequência de caracteres, que pode conter letras, números, símbolos e espaços. 
# As strings são delimitadas por aspas simples (') ou duplas ("), e até mesmo triplas (''' ou """) para strings multilinhas.

# As strings são amplamente utilizadas em Python para representar texto e são essenciais em muitos aspectos da programação, incluindo manipulação de arquivos, entrada/saída, processamento de texto, interface com o usuário, entre outros.

# str -> string -> texto

print(1234)

# Aspas Simples
print('Curso de Python')
print('Curso de "Python"')

# Aspas Duplas
print("Curso de Python")
print("Curso de 'Python'")