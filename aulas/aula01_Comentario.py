# Em Python, um comentário é um trecho de texto que não é executado pelo interpretador durante a execução do programa. 
# Comentários são utilizados para fornecer explicações, documentar código e melhorar a compreensão do código-fonte. 
# Eles começam com o caractere # e podem aparecer em uma linha própria ou ao final de uma linha de código. 
# Os comentários são completamente ignorados pelo interpretador Python, sendo úteis para fazer anotações, explicar o propósito de determinado bloco de código ou desativar temporariamente parte do código sem removê-lo. 
# O uso de comentários contribui para a legibilidade e manutenção do código.


# Permite escrever um comentário
print(123) # Comentário ao lado
# Comentário Abaixo
# print(123) Desativando trecho de código