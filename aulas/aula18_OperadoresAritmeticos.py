# OPERADORES ARITMÉTICOS

# As operações aritméticas em Python permitem realizar cálculos matemáticos básicos e avançados. 
# Aqui está um resumo das operações aritméticas fundamentais em Python:


# Adição (+): Realiza a adição de dois valores.
resultado = 5 + 3                   # Resultado é 8
print('Adição:', resultado)         # Saída: Adição: 8


# Subtração (-): Subtrai o segundo valor do primeiro.
resultado = 5 - 3                   # Resultado é 2
print('Subtração:', resultado)      # Saída: Subtração: 2


# Multiplicação (*): Realiza a multiplicação de dois valores.
resultado = 5 * 3                   # Resultado é 15
print('Multiplicação:', resultado)  # Saída: Multiplicação: 15


# Divisão (/): Divide o primeiro valor pelo segundo, retornando um resultado de ponto flutuante.
resultado = 5 / 3                   # Resultado é 1.6666666666666667
print('Divisão:', resultado)        # Saída: Divisão: 1.6666666666666667


# Divisão Inteira (//): Divide o primeiro valor pelo segundo e retorna apenas a parte inteira do resultado.
resultado = 5 // 3                  # Resultado é 1
print('Divisão Inteira:', resultado)# Saída: Divisão Inteira: 1


# Exponenciação (**): Eleva o primeiro valor à potência do segundo.
resultado = 5 ** 3                  # Resultado é 125
print('Exponenciação:', resultado)  # Saída: Exponenciação: 125


# Módulo (%): Retorna o resto da divisão do primeiro valor pelo segundo.
resultado = 5 % 3                   # Resultado é 2
print('Módulo:', resultado)         # Saída: Módulo: 2


# Essas operações aritméticas são amplamente utilizadas em Python para realizar cálculos matemáticos em programas. 
# É importante lembrar a ordem de precedência das operações (PEMDAS/BODMAS) ao escrever expressões matemáticas complexas.