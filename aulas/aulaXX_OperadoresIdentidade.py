# Operadores de Identidade (Associação) IN e NOT IN

'''
Os operadores in e not in em Python são utilizados para verificar se um valor está presente ou não em uma sequência, como uma lista, tupla, string, dicionário ou conjunto. Aqui está um resumo sobre esses operadores:

Operador in:

O operador in retorna True se o valor estiver presente na sequência.
É usado para verificar a existência de um elemento dentro de uma sequência.

lista = [1, 2, 3, 4, 5]
resultado = 3 in lista  # Resultado é True

Neste exemplo, resultado será True, pois o valor 3 está presente na lista.

Operador not in:

O operador not in retorna True se o valor não estiver presente na sequência.
É usado para verificar se um elemento não está presente dentro de uma sequência.

lista = [1, 2, 3, 4, 5]
resultado = 6 not in lista  # Resultado é True

Neste exemplo, resultado será True, pois o valor 6 não está presente na lista.
Esses operadores são úteis para realizar verificações de pertencimento em coleções de dados e são frequentemente usados em expressões condicionais, loops e expressões booleanas em Python. Eles oferecem uma maneira concisa e legível de verificar a presença de um valor em uma sequência ou sua ausência.
'''