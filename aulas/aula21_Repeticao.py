# REPETIÇÃO

# A repetição de strings refere-se ao processo de criar uma nova string que consiste na repetição de uma string existente várias vezes. 
# Isso pode ser feito utilizando o operador de multiplicação *.

texto = "Olá "
repeticao = texto * 3
print(repeticao)  # Saída: Olá Olá Olá 


# Neste exemplo, a string "Olá " é repetida três vezes, resultando na string "Olá Olá Olá ".
# A repetição de strings é útil em situações onde você precisa criar strings que contenham padrões repetitivos, como espaços em branco, caracteres de preenchimento, ou partes de uma saída formatada. 
# É uma técnica simples, porém poderosa, para construir strings com base em padrões repetitivos de forma eficiente em Python.