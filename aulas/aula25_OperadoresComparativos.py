# Operadores de Comparação (Relacionais)

# Os operadores de comparação em Python são usados para comparar dois valores e determinar a relação entre eles. 
# Eles retornam um valor booleano (True ou False) com base no resultado da comparação. 
# Aqui está um resumo dos operadores de comparação em Python:

# Igualdade (==)
# Verifica se dois valores são iguais.
x = 5
y = 5
resultado = x == y  # Resultado é True

# Diferença (!=)
# Verifica se dois valores são diferentes.
x = 5
y = 10
resultado = x != y  # Resultado é True

# Maior que (>)
# Verifica se o primeiro valor é maior que o segundo.
x = 10
y = 5
resultado = x > y  # Resultado é True

# Maior ou igual (>=)
# Verifica se o primeiro valor é maior ou igual ao segundo.
x = 10
y = 10
resultado = x >= y  # Resultado é True

# Menor que (<)
# Verifica se o primeiro valor é menor que o segundo.
x = 5
y = 10
resultado = x < y  # Resultado é True

# Menor ou igual (<=)
# Verifica se o primeiro valor é menor ou igual ao segundo.
x = 5
y = 5
resultado = x <= y  # Resultado é True

# Esses operadores de comparação são amplamente utilizados para criar expressões lógicas e controlar o fluxo do programa com base nas relações entre os valores. 
# Eles são essenciais para construir declarações condicionais e loops.






