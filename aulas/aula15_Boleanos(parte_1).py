# BOOLEAN

# Em Python, um valor booleano (bool) representa um valor lógico que pode ser verdadeiro (True) ou falso (False). 
# Esse tipo de dado é comumente utilizado em expressões condicionais e operações lógicas, fornecendo a base para o controle de fluxo em programas.

# Operações lógicas: Os valores booleanos podem ser combinados usando operadores lógicos, como and, or e not, para formar expressões mais complexas.

# Expressões em Python são avaliadas como verdadeiras ou falsas. 
# Por exemplo, números diferentes de zero, strings não vazias e listas não vazias são considerados verdadeiros, enquanto zero, strings vazias e listas vazias são considerados falsos.

# bool -> boolean -> booleano

print(bool(20))                 # True (verdadeiro)
print(bool(0))                  # False (falso)
print(bool(""))                 # False (falso)
print(bool("String não vazia")) # True (Verdadeiro)

# O tipo booleano desempenha um papel fundamental na lógica de programação em Python, sendo utilizado em condições, loops, expressões condicionais e em muitas outras situações onde é necessário avaliar a veracidade ou falsidade de uma expressão.