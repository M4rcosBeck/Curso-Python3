# CONCATENAÇÃO

# Em Python, a concatenação é o processo de combinar duas ou mais strings em uma única string. 
# Isso é feito usando o operador de adição (+).
# Existe outra forma de realizar a concatenação através do método join(), mas veremos isso mais à frente.

# O operador de adição (+) pode ser usado para concatenar strings. Ele simplesmente une duas strings em uma nova string.
saudacao = "Olá, "
nome = "Maria!"
print(saudacao + nome)  # Saída: "Olá, Maria!"

concatenacao = "Python" + " é " + "incrível!"
print(concatenacao)     # Saída: Python é incrível!

# A concatenação é uma operação comum em Python e é amplamente utilizada para combinar strings para formar mensagens, construir caminhos de arquivos, formatar saídas de texto e muito mais. 
# É uma maneira eficaz de manipular e construir strings dinamicamente em seus programas Python.