# A função print em Python é uma função embutida que permite exibir informações na saída padrão, geralmente no console ou terminal. Sua sintaxe básica é print(valor1, valor2, ...), onde os valores são os elementos ou variáveis que você deseja exibir. A função print aceita vários argumentos separados por vírgulas e os imprime na ordem em que são fornecidos.

print("Olá, Mundo!")

# Neste caso específico, o texto "Olá, Mundo!" está entre parênteses e aspas, indicando que é uma cadeia de caracteres (string).

print(12, 34, 56, 78) 

# É importante notar que, por padrão, a função print adiciona um espaço entre cada argumento e automaticamente adiciona uma quebra de linha no final, a menos que seja especificado de outra forma.

print('argumento 1', 'argumento 2')
print('argumento 3', 'argumento 4')

# A função print é fundamental para depuração, exibição de resultados, e é comumente usada para interação simples com o usuário. Além disso, é frequentemente utilizada em programas Python para fornecer feedback e informações visuais durante a execução do código.

# Cada um dos argumentos da função print são chamados de 'argumentos não-nomeados'.