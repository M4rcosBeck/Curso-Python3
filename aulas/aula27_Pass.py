# PASS

# A palavra-chave pass é usada em Python para criar um bloco de código vazio. 
# Ela não faz nada e é usada como um espaço reservado onde o código futuro pode ser adicionado.
# É útil quando a sintaxe requer um bloco de código, mas nenhuma ação é necessária.
# Pode ser usado em loops, condicionais, funções e classes onde nenhum código adicional é necessário.

condicao = True

if condicao:
    pass  # Bloco vazio, nenhum código executado
else:
    print("Condição é falsa")

# Em resumo, pass é um recurso útil em Python para situações em que é necessário criar um bloco de código vazio como espaço reservado (pass). 
# Eles ajudam a melhorar a legibilidade e a clareza do código em situações específicas.